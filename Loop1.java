package com.basics.program;

public class Loop1 {

	public static void main(String[] args) {
	
		// ascci to alphabet by using for loop
		for(int a=97;a<=122;a++)
		{
			char c=(char)a;
		
		int b=a-32;
		char d=(char)b;
		System.out.println(c+ "=" +d);
		
		}

	}

}
