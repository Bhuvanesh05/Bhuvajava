class SwitchCase
{
public static void main(String args[])
{
int a=7;

switch(a)
{
 case 7:
 case 8:
   System.out.println("hollyday");
break;
case 1:
case 2:
case 3:
case 4:
case 5:
System.out.println("weekdays");
break;
 default :
   System.out.println("invalid");
break;
}
}
}